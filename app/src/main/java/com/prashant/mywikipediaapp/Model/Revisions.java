package com.prashant.mywikipediaapp.Model;

public class Revisions {
    private String contentformate;
    private String contentmodel;
    private String content;

    public Revisions() {
    }

    public String getContentformate() {
        return contentformate;
    }

    public void setContentformate(String contentformate) {
        this.contentformate = contentformate;
    }

    public String getContentmodel() {
        return contentmodel;
    }

    public void setContentmodel(String contentmodel) {
        this.contentmodel = contentmodel;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
